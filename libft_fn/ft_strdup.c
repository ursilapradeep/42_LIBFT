#include "libft.h"
#include <stdlib.h>
#include <string.h>

char	*ft_strdup(const char *s1)
{
	char	*ptr;
	size_t	number_of_bytes;
	size_t	i;

	i = 0;
	number_of_bytes = ft_strlen(s1);
	ptr = (char *)malloc(number_of_bytes + 1);
	if (!ptr)
		return (NULL);
	while (s1[i])
	{
		ptr[i] = s1[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
